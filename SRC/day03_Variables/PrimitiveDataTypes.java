package day03_Variables;

public class PrimitiveDataTypes {
	/*
	 * variable name has to start with alphabet a-z, A-Z
	 * variable name cannot contain any special characters except _ and $
	 * variable name must be unique 
	 * variable name cannot use reserved java words 
	 * byte:
	 * can assign whole numbers, between -128 and 127
	 * 
	 * Short:
	 * we can assign whole numbers between -32768 and 32767
	 * 
	 * int:
	 * we can assign whole numbers between -2147483648 and 2147483647
	 * 
	 * long:
	 * It accepts letter L,l at the end of the number 
	 * 
	 * byte< short< int< long
	 * 
	 * 
	 * small data type can assign to larger data type 
	 *
	 *
	 *float: can accept decimals with letter f or F at the end
	 *
	 *double: can accept decimals and is used more often. 
	 *
	 *float is smaller than double. 
	 *
	 */
     public static void main(String[] args){
		
    	 byte length = 3;
    	 System.out.println(length);
    	 

    	 byte width = 4; 
    	 System.out.println(width);

     
     }
    	 
}
 
